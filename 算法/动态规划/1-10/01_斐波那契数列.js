// 509. 斐波那契数

// 动规五部曲
// 1-确定dp数组及dp数组每一项的含义
// 我们要用一个一维dp数组来保存递归的结果，dp[i]便是当前的斐波那契数
// 2-确定递归公式
// dp[i]=dp[i-1]+dp[i-2]
// 3-dp数组初始化
// dp[0]=0  dp[1]]=1
// 4-确定遍历顺序   从前向后递归
// 5-举例推导dp数组

function fib(n) {
  let dp = [0, 1]
  for (let i = 2; i <= n; i++) {
    dp[i] = dp[i - 1] + dp[i - 2]
  }
  return dp[n]
}